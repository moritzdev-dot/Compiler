section .data
.S0: 
	DB "%d", 0

section .text
global main
extern printf
print: 
	PUSH RBP
	MOV RBP, RSP
	MOV RDI, [RBP + 16]
	MOV RSI, [RBP + 24]
	XOR RAX, RAX
	CALL printf
	LEAVE 
	RET 
f: 
	PUSH RBP
	MOV RBP, RSP
	SUB RSP, 16
	MOV RAX, QWORD [RBP + 16]
	MOV QWORD [RBP - 16], RAX
	MOV RAX, QWORD [rbp-16]
	PUSH RAX
	PUSH 5
	POP RBX
	POP RAX
	CMP RAX, RBX
	SETG AL
	MOVZX RAX, AL
	PUSH RAX
	POP RAX
	CMP RAX, 0
	JE .A0
	MOV RAX, QWORD [rbp-16]
	PUSH RAX
	LEAVE 
	RET 
	JMP .A1
.A0: 
	PUSH 10
	LEAVE 
	RET 
.A1: 
	MOV RSP, RBP
	POP RBP
	RET 
main: 
	PUSH RBP
	MOV RBP, RSP
	SUB RSP, 16
	PUSH 5
	CALL f
	MOV RAX, .S0
	PUSH RAX
	CALL print
	XOR EAX, EAX
	LEAVE 
	RET 
